/**
 * This structure represents a note event in a music piece
 */
struct note
{
	uint32_t midi_msg;
	uint32_t duration_ms;
};

/**
 * This structure represents a music piece
 * Length is given in # of notes
 */
struct piece
{
	size_t length;
	struct note *sheet;
};

